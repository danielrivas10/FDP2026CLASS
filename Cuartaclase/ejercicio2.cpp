#include <iostream>
using namespace std;

int main() {
    int contador = 1;

    // Ciclo do-while, hace y luego verifica
    do {
        cout << "Contador en do-while: " << contador << endl;
        contador++; // Incrementa el contador
    } while (contador <= 5);
cout <<"salio del bucle";
    return 0;
}